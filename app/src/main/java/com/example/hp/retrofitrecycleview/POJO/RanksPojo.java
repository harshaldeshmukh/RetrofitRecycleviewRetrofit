package com.example.hp.retrofitrecycleview.POJO;

public class RanksPojo {
    public  String sr;
    public  String rank;

    public RanksPojo(String sr, String rank) {
        this.sr = sr;
        this.rank = rank;
    }

    public String getSr() {
        return sr;
    }

    public String getRank() {
        return rank;
    }
}
